    public boolean TesteAutenticacao() throws ParseException {
        //verifica se o software está licenciado
        try {
            EmpresaDAO dao = new EmpresaDAO();
            if (dao.buscarInfoEmpresa(Dados.Read("Serial"))) {
//                System.out.println("serial: " + Dados.Read("Serial"));
                lbEmpresa.setText(empresa);
//                System.out.println("empresa2: " + empresa);
                lbAutenticacao.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icons/icnVerificado.png")));
                return true;
            } else {
                lbEmpresa.setText("Não Licenciado");
                return false;
            }
        } catch (IOException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            return false;
        }
    }

