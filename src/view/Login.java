    private void btEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntrarActionPerformed
        // TODO add your handling code here:

        try {
            if (TesteServidor()) {
                if (TesteAutenticacao()) {
                    String login, senha;
                    login = tfLogin.getText();
                    senha = tfSenha.getText();
                    FuncionariosDAO dao = new FuncionariosDAO();
                    if (dao.Logar(login, senha)) {
                        Home home = new Home();
                        home.setExtendedState(MAXIMIZED_BOTH);
                        home.setVisible(true);
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(null, "Dados de login incorretos.");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Software não licenciado."
                            + " Entre em contato conosco para resolvermos este problema.");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Sem acesso ao banco de dados. Verifique a sua internet e tente novamente");
            }

        } catch (Exception e) {
            JOptionPane.showInputDialog(e, null);
        }

    }//GEN-LAST:event_btEntrarActionPerformed

