    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:

        Clientes clientes = new Clientes();
        clientes.setNome(tfNome.getText());
        clientes.setCelular(tfCelular.getText());
        clientes.setEmail(tfEmail.getText());
        if (pFisica.isSelected()) {
            clientes.setPessoa("Física");
            clientes.setCpfcnpj(tfCpf.getText());
        } else if (pJuridica.isSelected()) {
            clientes.setPessoa("Jurídica");
            clientes.setCpfcnpj(tfCnpj.getText());
        }
        clientes.setIe(tfIe.getText());
        clientes.setEndereco(tfEndereco.getText());
        clientes.setNumero(Integer.parseInt(tfNumero.getText()));
        clientes.setComplemento(tfComplemento.getText());
        clientes.setReferencia(tfReferencia.getText());
        clientes.setBairro(tfBairro.getText());
        clientes.setCidade(tfCidade.getText());

        ClientesDAO clientesDAO = new ClientesDAO();
        clientesDAO.cadastrarCliente(clientes);
        listTable();

        new util.Util().LimpaTela(abaClientes);
    }//GEN-LAST:event_btSalvarActionPerformed

