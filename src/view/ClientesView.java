    private void tbClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbClientesMouseClicked
        // TODO add your handling code here:
        jTabbedPane1.setSelectedIndex(0);

        ClientesDAO clientesDAO = new ClientesDAO();
        Clientes cliente = new Clientes();

        cliente = clientesDAO.exibirCliente(tbClientes.getValueAt(tbClientes.getSelectedRow(), 0).toString());

        tfId.setText(String.valueOf(cliente.getId()));
        tfNome.setText(cliente.getNome());
        tfCelular.setText(cliente.getCelular());
        tfEmail.setText(cliente.getEmail());
        if (cliente.getPessoa().equals("Jurídica")) {
            Pessoa("Jurídica");
            tfCnpj.setText(cliente.getCpfcnpj());
            tfIe.setText(cliente.getIe());
        } else {
            Pessoa("Física");
            tfCpf.setText(cliente.getCpfcnpj());
        }
        tfEndereco.setText(cliente.getEndereco());
        tfNumero.setText(String.valueOf(cliente.getNumero()));
        tfComplemento.setText(cliente.getComplemento());
        tfReferencia.setText(cliente.getReferencia());
        tfBairro.setText(cliente.getBairro());
        tfCidade.setText(cliente.getCidade());

    }//GEN-LAST:event_tbClientesMouseClicked

