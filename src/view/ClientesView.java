    public void Pessoa(String pessoa) {
        if (pessoa.equals("Jurídica")) {
            pJuridica.setSelected(true);
            pFisica.setSelected(false);
            tfIe.setVisible(true);
            lbIE.setVisible(true);
            cnpj.setVisible(true);
            tfCnpj.setVisible(true);
            cpf.setVisible(false);
            tfCpf.setVisible(false);
        } else {
            pJuridica.setSelected(false);
            pFisica.setSelected(true);
            tfIe.setVisible(false);
            lbIE.setVisible(false);
            cnpj.setVisible(false);
            tfCnpj.setVisible(false);
            cpf.setVisible(true);
            tfCpf.setVisible(true);
        }
    }

