    public void finalizar() {
        
        //ultima venda realizada
        VendasDAO daoV = new VendasDAO();
        int idVenda = daoV.ultimaVenda();
        
        for(int i = 0; i<carrinho.getRowCount();i++){
            
            ItensVenda item = new ItensVenda();
            item.setVenda(idVenda);
            item.setProduto(itens.get(i));
            item.setQtd(Integer.parseInt(carrinho.getValueAt(i, 2).toString()));
            item.setTotal(Double.parseDouble(carrinho.getValueAt(i, 4).toString().replaceAll(",", ".")));
            
            ItemVendaDAO daoI = new ItemVendaDAO();
            daoI.cadastraItem(item);
        }
        
        if (!tfDinheiro.getText().equals("")) {
            pDinheiro = Double.parseDouble(tfDinheiro.getText().replaceAll(",", "."));
        }
        if (!tfCredito.getText().equals("")) {
            pCredito = Double.parseDouble(tfCredito.getText().replaceAll(",", "."));
        }
        if (!tfDebito.getText().equals("")) {
            pDebito = Double.parseDouble(tfDebito.getText().replaceAll(",", "."));
        }
        if (!tfCheque.getText().equals("")) {
            pCheque = Double.parseDouble(tfCheque.getText().replaceAll(",", "."));
        }
        if (!tfVale.getText().equals("")) {
            pVale = Double.parseDouble(tfVale.getText().replaceAll(",", "."));
        }

        totalPg = pDinheiro + pCredito + pDebito + pCheque + pVale;

        if (total > totalPg) {
            JOptionPane.showMessageDialog(null, "Pagamento incompleto.");
        } else {
            troco = totalPg - total;
//            JOptionPane.showMessageDialog(null, "Troco: R$" + String.valueOf(df.format(troco)).replaceAll("\\.", ","));
            PDVView pdv = new PDVView();
//            pdv.troco(troco);
            PDVView.troco = troco;
            dispose();
        }

    }

    public void Pessoa(String pessoa) throws ParseException {
        switch (pessoa) {
            case "Física":
                rbFisica.setSelected(true);
                rbJuridica.setSelected(false);
                lbCpf.setText("CPF:");
                tfCpf.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(
                        new javax.swing.text.MaskFormatter("###.###.###-##")));
                break;
            case "Jurídica":
                rbFisica.setSelected(false);
                rbJuridica.setSelected(true);
                lbCpf.setText("CNPJ:");
                tfCpf.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(
                        new javax.swing.text.MaskFormatter("##.###.###/####-##")));
                break;
        }
    }

