    private void tfDinheiroKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfDinheiroKeyPressed
        // dinheiro
        switch (evt.getKeyCode()) {
            case KeyEvent.VK_F1:
                //dinheiro
                tfDinheiro.requestFocus();
                break;
            case KeyEvent.VK_F2:
                //crédito
                tfCredito.requestFocus();
                break;
            case KeyEvent.VK_F3:
                //débito
                tfDebito.requestFocus();
                break;
            case KeyEvent.VK_F4:
                //cheque
                tfCheque.requestFocus();
                break;
            case KeyEvent.VK_F5:
                //vale
                tfVale.requestFocus();
                break;
            case KeyEvent.VK_ENTER:
                //finalizar
                if (!tfVale.getText().equals("")) {
                    if (tfCliente.getText().equals("")) {
                        JOptionPane.showMessageDialog(null, "Para vendas utilizando a opção de vale, deve-se obrigatoriamente informar um cliente");
                    } else {
                        finalizar();
                    }
                } else {
                    finalizar();
                }

                break;

        }
    }//GEN-LAST:event_tfDinheiroKeyPressed

