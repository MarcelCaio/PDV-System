    private void tbFuncionariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbFuncionariosMouseClicked
        // TODO add your handling code here:
        jTabbedPane1.setSelectedIndex(0);

        FuncionariosDAO funcionariosDAO = new FuncionariosDAO();
        Funcionarios funcionarios = new Funcionarios();

        funcionarios = (Funcionarios) funcionariosDAO.exibirFuncionario(tbFuncionarios.getValueAt(tbFuncionarios.getSelectedRow(), 0).toString());

        tfId.setText(String.valueOf(funcionarios.getId()));
        tfNome.setText(funcionarios.getNome());
        tfLogin.setText(funcionarios.getLogin());
        tfCelular.setText(funcionarios.getCelular());
        tfEmail.setText(funcionarios.getEmail());
        tfEndereco.setText(funcionarios.getEndereco());
        tfNumero.setText(String.valueOf(funcionarios.getNumero()));
        tfComplemento.setText(funcionarios.getComplemento());
        tfBairro.setText(funcionarios.getBairro());
        tfCidade.setText(funcionarios.getCidade());
        tfRg.setText(funcionarios.getRg());
        tfCpf.setText(funcionarios.getCpfcnpj());
        tfFuncao.setText(funcionarios.getFuncao());
        for (int i = 0; i <= cbNivel.getMaximumRowCount(); i++) {
            if (funcionarios.getAcesso().equals(cbNivel.getItemAt(i))) {
                cbNivel.setSelectedIndex(i);
            }
        }

    }//GEN-LAST:event_tbFuncionariosMouseClicked

