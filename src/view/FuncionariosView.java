    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        // TODO add your handling code here:
        if (Senha()) {
            Funcionarios funcionarios = new Funcionarios();
            funcionarios.setNome(tfNome.getText());
            funcionarios.setLogin(tfLogin.getText());
            funcionarios.setRg(tfRg.getText());
            funcionarios.setCpfcnpj(tfCpf.getText());
            funcionarios.setEmail(tfEmail.getText());
            funcionarios.setSenha(tfSenha.getText());
            funcionarios.setFuncao(tfFuncao.getText());
            funcionarios.setAcesso(cbNivel.getSelectedItem().toString());
            funcionarios.setCelular(tfCelular.getText());
            funcionarios.setEndereco(tfEndereco.getText());
            funcionarios.setNumero(Integer.parseInt(tfNumero.getText()));
            funcionarios.setComplemento(tfComplemento.getText());
            funcionarios.setBairro(tfBairro.getText());
            funcionarios.setCidade(tfCidade.getText());
            funcionarios.setId(Integer.parseInt(tfId.getText()));

            FuncionariosDAO funcionariosDAO = new FuncionariosDAO();
            funcionariosDAO.alterarFuncionario(funcionarios);
            listTable();

            new util.Util().LimpaTela(abaCadastrarFuncio);
        } else {
            JOptionPane.showMessageDialog(null, "Senhas vazias ou não coincidem.");
        }
    }//GEN-LAST:event_btEditarActionPerformed

