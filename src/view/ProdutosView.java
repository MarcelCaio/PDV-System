    private void tfLucroPercenKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfLucroPercenKeyReleased
        // TODO add your handling code here:
        double venda;
        DecimalFormat df = new DecimalFormat("0.00");

        venda = Double.parseDouble(tfPrecoCompra.getText().replaceAll(",", "."))
                * (1 + (Double.parseDouble(tfLucroPercen.getText().replaceAll(",", "."))) / 100);
        tfPrecoVenda.setText(String.valueOf(df.format(venda)).replaceAll("\\.", ","));
        tfLucroReal.setText(String.valueOf(df.format(
                venda - Double.parseDouble(tfPrecoCompra.getText().replaceAll(",", ".")))
        ).replaceAll("\\.", ","));

    }//GEN-LAST:event_tfLucroPercenKeyReleased

    private void tfLucroRealKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfLucroRealKeyReleased
        // TODO add your handling code here:
        double venda;
        DecimalFormat df = new DecimalFormat("0.00");

        venda = Double.parseDouble(tfPrecoCompra.getText().replaceAll(",", "."))
                + (Double.parseDouble(tfLucroReal.getText().replaceAll(",", ".")));

        tfPrecoVenda.setText(String.valueOf(df.format(venda)).replaceAll("\\.", ","));

        tfLucroPercen.setText(String.valueOf(df.format(
                ((Double.parseDouble(tfPrecoVenda.getText().replaceAll(",", "."))
                / Double.parseDouble(tfPrecoCompra.getText().replaceAll(",", "."))) - 1) * 100)));
    }//GEN-LAST:event_tfLucroRealKeyReleased

