    private void tfProdutoPesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfProdutoPesquisaKeyReleased
        // TODO add your handling code here:

        ProdutosDAO dao = new ProdutosDAO();
        String pesquisa = "'%" + tfProdutoPesquisa.getText() + "%'";
        DecimalFormat df = new DecimalFormat("0.00");
        List<Produtos> lista = dao.PesquisarProdutos(cbItemPesquisa.getSelectedItem().toString(), pesquisa);

        DefaultTableModel dados = (DefaultTableModel) tbProdutos.getModel();
        dados.setNumRows(0);
        for (Produtos c : lista) {
            String venda = df.format(c.getPrecoVenda());
            String estoque = c.getEstoque() + " " + c.getUnidade();
            String pVenda = "R$ " + venda.replaceAll("\\.", ",");
            dados.addRow(new Object[]{
                c.getCodBarras(),
                c.getDescricao(),
                pVenda,
                estoque,
                c.getFornecedores().getNome()
            });
        }
    }//GEN-LAST:event_tfProdutoPesquisaKeyReleased

