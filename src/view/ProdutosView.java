    private void atualizarListaFornecedores() {
        FornecedoresDAO dao = new FornecedoresDAO();
        List<Fornecedores> listaFornecedores = dao.listarFornecedores();
        cbFornecedor.removeAll();
        for (Fornecedores f : listaFornecedores) {
            cbFornecedor.addItem(f);
        }
    }

    public void listTable() {
        ProdutosDAO dao = new ProdutosDAO();
        List<Produtos> lista = dao.listarFuncionarios();
        DecimalFormat df = new DecimalFormat("0.00");

        DefaultTableModel dados = (DefaultTableModel) tbProdutos.getModel();
        dados.setNumRows(0);

        for (Produtos c : lista) {
            String venda = df.format(c.getPrecoVenda());
            String estoque = c.getEstoque() + " " + c.getUnidade();
            String pVenda = "R$ " + venda.replaceAll("\\.", ",");
            dados.addRow(new Object[]{
                c.getCodBarras(),
                c.getDescricao(),
                pVenda,
                estoque,
                c.getFornecedores().getNome()
            });
        }
    }

